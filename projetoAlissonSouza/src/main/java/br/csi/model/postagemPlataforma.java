package br.csi.model;

public class postagemPlataforma {
		
		private Long codPost;
		private Long codTipoPlataforma;
		
		public Long getCodPost() {
			return codPost;
		}
		public void setCodPost(Long codPost) {
			this.codPost = codPost;
		}
		public Long getCodTipoPlataforma() {
			return codTipoPlataforma;
		}
		public void setCodTipoPlataforma(Long codTipoPlataforma) {
			this.codTipoPlataforma = codTipoPlataforma;
		}
}

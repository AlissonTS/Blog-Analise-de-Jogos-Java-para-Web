package br.csi.model;

public class postagemJogo {

		private Long codPost;
		private Long codTipoJogo;
		
		public Long getCodPost() {
			return codPost;
		}
		public void setCodPost(Long codPost) {
			this.codPost = codPost;
		}
		public Long getCodTipoJogo() {
			return codTipoJogo;
		}
		public void setCodTipoJogo(Long codTipoJogo) {
			this.codTipoJogo = codTipoJogo;
		}
}
